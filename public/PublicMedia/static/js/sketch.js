function setup() {
  createCanvas(windowWidth, windowHeight, WEBGL);
  len = windowWidth / 15;
  let idx = 0;
  for (let i = 0; i < dim; i++) {
    cube[i] = [];
    for (let j = 0; j < dim; j++) {
      cube[i][j] = [];
      for (let k = 0; k < dim; k++) {
        cube[i][j][k] = new Cubie(k, i, j, len, cubies[idx]) // 0, 0, 0, len, ['W', 'Y', 'G', 'B', 'R', 'O']
        idx += 1;
      }
    }
  }
}

function draw() {
  push();
  box(len + 10)
  pop();
  background(51);
  orbitControl();
  for (let i = 0; i < dim; i++) {
    for (let j = 0; j < dim; j++) {
      for (let k = 0; k < dim; k++) {
        cube[i][j][k].show();
      }
    }
  }

}